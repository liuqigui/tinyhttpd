/**********************************************************************/
/* Send a regular file to the client.  Use headers, and report
 * errors to client if they occur.
 * Parameters: a pointer to a file structure produced from the socket
 *              file descriptor
 *             the name of the file to serve */
/**********************************************************************/
void serve_file(int client, const char *filename) //调用 cat 把服务器文件返回给浏览器。
{
 FILE *resource = NULL;
 int numchars = 1;
 char buf[1024];

 buf[0] = 'A'; buf[1] = '\0';
 while ((numchars > 0) && strcmp("\n", buf))  /* read & discard headers */
  numchars = get_line(client, buf, sizeof(buf));

 resource = fopen(filename, "r");
 if (resource == NULL)
  not_found(client);
 else
 {
  headers(client, filename);
  cat(client, resource);
 }
 fclose(resource);
}

/**********************************************************************/
/* This function starts the process of listening for web connections
 * on a specified port.  If the port is 0, then dynamically allocate a
 * port and modify the original port variable to reflect the actual
 * port.
 * Parameters: pointer to variable containing the port to connect on
 * Returns: the socket */
/**********************************************************************/
int startup(u_short *port) //初始化 httpd 服务，包括建立套接字，绑定端口，进行监听等。
{
 int httpd = 0;
 struct sockaddr_in name; //定义在<netinet/in.h>中，是一个结构体，用于存储IPv4地址信息
// 创建服务器端socket PF_INET 地址类型ipv4，SOCK_STREAM socket的类型，0前面类型参数的默认协议
 httpd = socket(PF_INET, SOCK_STREAM, 0);
 if (httpd == -1) //错误判断
  error_die("socket");
 memset(&name, 0, sizeof(name));  //fill a byte string with a byte,value,填充字符为0
 name.sin_family = AF_INET; //地址类型ipv4
 name.sin_port = htons(*port);  //端口转换为网络字节序（大端存储）
 name.sin_addr.s_addr = htonl(INADDR_ANY);  //本机任一可用ip地址
//  intet_pton(AF_INET,"192.168.1.1",&name.sin_addr.s_addr);
 if (bind(httpd, (struct sockaddr *)&name, sizeof(name)) < 0) //绑定地址
  error_die("bind");
 if (*port == 0)  /* if dynamically allocating a port 传递的端口号如果是0，则随机选取可用的端口*/
 {
  socklen_t namelen = sizeof(name);
  if (getsockname(httpd, (struct sockaddr *)&name, &namelen) == -1)
   error_die("getsockname");
  *port = ntohs(name.sin_port); //修改port
 }
 if (listen(httpd, 5) < 0)  //服务器开始监听
  error_die("listen");
 return(httpd); //返回服务器socket描述符
}

